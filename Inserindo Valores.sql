/* Seleção -> É um Subconjunto do Conjunto Total de Registros de uma Tabela a Clausula de Seleção é o WHERE */
SELECT
	NOME,
	SEXO,
	EMAIL /* Projeção */
FROM
	CLIENTE /* Origem */
WHERE
	SEXO = 'F'; /* Seleção */


SELECT
	NUMERO /* Projeção */
FROM
	TELEFONE /* Origem */
WHERE
	TIPO = 'CEL'; /* Seleção */


/* Junção -> JOIN */
SELECT
	NOME,
	EMAIL,
	IDCLIENTE
FROM
	CLIENTE;


SELECT
	ID_CLIENTE;
	BAIRRO,
	CIDADE
FROM
	ENDERECO;


SELECT
	NOME,
	SEXO,
	BAIRRO,
	CIDADE
FROM
	CLIENTE
INNER JOIN
	ENDERECO
ON
	IDCLIENTE = ID_CLIENTE;


SELECT
	NOME,
	SEXO,
	BAIRRO,
	CIDADE /* Projeção */
FROM
	CLIENTE /* Origem */
INNER JOIN
	ENDERECO /* Junção */
ON
	IDCLIENTE = ID_CLIENTE
WHERE
	SEXO = 'F'; /* Seleção */


